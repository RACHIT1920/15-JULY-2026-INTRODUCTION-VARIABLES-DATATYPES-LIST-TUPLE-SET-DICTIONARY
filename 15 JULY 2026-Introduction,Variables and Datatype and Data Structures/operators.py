# Arithmetic operators

a=10
b=8

print(a+b)
print(a-b)
print(a*b)
# division will give output in float
print(a/b)
# floor division will give output in integer
print(a//b)
# will give remainder
print(a%b)
# exponentiation
print(a**b)


# Comparison operators

c=19
d=18

print(c>d)
print(c==d)
print(c!=d)
print(c>=d)

# Logical Operators

print(a>d and b<c)
print(a<b or c>d)
print(not(a>b))